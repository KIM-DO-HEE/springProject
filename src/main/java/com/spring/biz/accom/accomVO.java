package com.spring.biz.accom;

public class accomVO {
	private int accomId;
	private String name;
	private String address;
	private String accomExplain;
	private String type;
	
	public int getAccomId() {
		return accomId;
	}
	public void setAccomId(int accomId) {
		this.accomId = accomId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getAccomExplain() {
		return accomExplain;
	}
	public void setAccomExplain(String accomExplain) {
		this.accomExplain = accomExplain;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
